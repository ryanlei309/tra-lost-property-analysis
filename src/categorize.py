DEFAULT = ("其他", "未知", False)

# 大類（category_group）：細類 -> 大類
CATEGORY_GROUP = {
    "交通票卡": "證件票卡", "信用卡證件": "證件票卡",
    "3C電子": "3C電子",
    "現金": "現金",
    "飾品": "服飾隨身", "眼鏡": "服飾隨身", "衣帽配件": "服飾隨身", "袋包": "服飾隨身",
    "雨具": "生活用品", "瓶罐水壺": "生活用品", "鑰匙": "生活用品", "書": "生活用品",
    "其他": "其他",
}


def classify(name: str):
    """回傳 (category, value_tier, recoverable)。"""
    n = (name or "").lower()
    for cat, kws, tier, recover in CATEGORY_RULES:
        if any(k.lower() in n for k in kws):
            return cat, tier, recover
    return DEFAULT


def group_of(category: str) -> str:
    return CATEGORY_GROUP.get(category, "其他")


