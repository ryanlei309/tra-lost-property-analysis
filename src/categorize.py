# (類別, [關鍵字], 價值層級, 是否通常可追回失主)
# 價值層級：高 / 中 / 低
# 可追回：True = 物品通常帶有可辨識失主的資訊（記名卡、證件等）
CATEGORY_RULES = [
    ("信用卡證件", ["信用卡", "金融卡", "提款卡", "身分證", "身份證", "健保卡",
                  "駕照", "護照", "證件", "員工證", "學生證"], "高", True),
    ("交通票卡",   ["悠遊卡", "一卡通", "icash", "愛金卡", "車票", "票卡", "月票"], "中", True),
    ("3C電子",     ["手機", "iphone", "ipad", "平板", "電腦", "筆電", "相機", "耳機",
                  "充電", "行動電源", "滑鼠", "鍵盤", "隨身碟", "手錶", "智慧"], "高", False),
    ("現金",       ["現金", "鈔", "零錢", "錢"], "高", False),
    ("眼鏡",       ["眼鏡", "墨鏡"], "中", False),
    ("袋包",       ["背包", "提袋", "手提", "包包", "袋", "包", "皮夾", "錢包", "皮包"], "中", False),
    ("鑰匙",       ["鑰匙", "鎖", "鎖匙"], "中", False),
    ("雨具",       ["傘"], "低", False),
    ("衣帽配件",   ["帽", "外套", "衣", "圍巾", "手套", "安全帽", "口罩", "圍兜"], "低", False),
    ("瓶罐水壺",   ["保溫瓶", "水瓶", "水壺", "瓶", "壺", "杯", "餐具"], "低", False),
]

DEFAULT = ("其他", "未知", False)


def classify(name: str):
    """回傳 (category, value_tier, recoverable)。name 為原始品名字串。"""
    n = (name or "").lower()
    for cat, kws, tier, recover in CATEGORY_RULES:
        if any(k.lower() in n for k in kws):
            return cat, tier, recover
    return DEFAULT


